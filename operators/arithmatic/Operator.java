class Operater{
    public static void main(String args[]){
        int num = 7;                    //ADDITION
        num += 9;
        System.out.println(num);

        int num1 = 17;                  //Substraction
        num1 -= 7;
        System.out.println(num1);

        int num2 = 7;                   //Multiplication
        num2 *= 9;
        System.out.println(num2);

        int num3 = 27;                  //Division
        num3 /= 9;
        System.out.println(num3);

        int num4 = 72;                  //Modulous
        num4 %= 9;
        System.out.println(num4);
    }
}