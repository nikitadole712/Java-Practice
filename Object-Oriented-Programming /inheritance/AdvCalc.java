class AdvCalc extends Calc  //AdvCalc is subclass
{           //we extend the properties of Calc(superclass)
            //his is single level inheritance
        public int mult(int n1, int n2)
        {
            return(n1 * n2);
        }
    
        public int div(int n1, int n2)
        {
            return(n1 / n2);
        }
}
