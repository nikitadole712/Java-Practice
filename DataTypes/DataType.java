class DataType{
    public static void main(String arg[]){
        int X = 10;
        int Y = 30;
        int Z = X + Y;
        System.out.println(Z);

        double d = 34.88;
        System.out.println(d);
        float f = 30f;
        System.out.println(f);

        char c = 'n';
        c++;
        System.out.println(c);

        boolean b = true;
        System.out.println(b);

        short sh = 567;
        System.out.println(sh);

        long l = 87_46_45_864l;
        System.out.println(l);
    }
}