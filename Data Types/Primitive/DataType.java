class DataType{
    public static void main(String arg[]){
        int X = 10;
        int Y = 30;             //Integer
        int Z = X + Y;
        System.out.println(Z);

        double d = 34.88;
        System.out.println(d);      //Double


        float f = 30f;
        System.out.println(f);      //float

        char c = 'n';
        c++;
        System.out.println(c);      //character

        boolean b = true;
        System.out.println(b);      //boolean

        short sh = 567;
        System.out.println(sh);     //short

        long l = 87_46_45_864l;
        System.out.println(l);      //long
    }
}